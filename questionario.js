// 2. Cos'è una variabile? prova a spiegarlo con parole tue, come se lo dovessi spiagare a qualcuno che
// non ne capisce niente di informatica.
//
// Risposta: Una variabile la possiamo immaginare come un contenitore il cui scopo sarà proprio quello
// di contenere qualcosa che sarà utilizzato al momento opportuno. Può contenere "cose" di tipo diverso e
// si può modificarne il contenuto nel tempo.
//
//
//
// 3. Undefined e null sono la stessa cosa?
// 
// Risposta: No, non sono la stessa cosa anche se apparentemente sembrano avere lo stesso comportamento.
// Undefined è il valore che assume una variabile di default quando non le si assegna nulla, null è un 
// valore che viene assegnato alla variabile di proposito.
