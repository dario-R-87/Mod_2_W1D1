// Risposta: un algoritmo è una sequenza ordinata di istruzioni, di azioni da compiere, affinché si possa portare 
// a termine un determinato compito. Un algoritmo può ricevere in ingresso dei dati , gli input, e può produrre
// dati in uscita, output.
